#pass statement in python is used when we want to write a code but we don't want to execute it. 
#It is a null statement that does nothing when executed. 
#It is often used as a placeholder for code that will be written later or to create an empty block of code.
#Example 1: Using pass statement in a function
for i in range(10):
    pass # This will do nothing and the loop will continue to the next iteration