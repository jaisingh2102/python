# Program to perform basic arithmetic operations (addition, subtraction, multiplication, division, modulus) on two numbers and display the results.
a = float(input("Enter the first number: "))
b = float(input("Enter the second number: "))
c = a+b
d = a-b
e = a*b
f = a/b
g = a%b
print("sum of a+b is:", c)
print("minus of a-b is", d)
print("multiply of a*b is", e)
print("divide of a/b is", f)
print("remainder of a%b is",g)
print(a ** b) #power operator